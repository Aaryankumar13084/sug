function getNutritionChartEnglish(week) {
    // Return exact week if available, otherwise find closest week
    if (nutritionChartEnglish[week]) {
        return nutritionChartEnglish[week];
    }

    // Find closest available week data based on trimester
    const trimester = week <= 12 ? 1 : week <= 27 ? 2 : 3;
    const weeks = Object.keys(nutritionChartEnglish).map(w => parseInt(w)).sort((a, b) => a - b);

    let closest = weeks[0];
    for (const w of weeks) {
        const wTrimester = w <= 12 ? 1 : w <= 27 ? 2 : 3;
        if (wTrimester === trimester) {
            closest = w;
        }
        if (w > week) break;
    }

    return nutritionChartEnglish[closest] || nutritionChartEnglish[1];
}

