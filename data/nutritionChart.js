function getNutritionChart(week, language = 'hindi') {
    if (language !== 'hindi') {
        return null; // English version in separate file
    }

    // Return exact week if available, otherwise find closest week
    if (nutritionChart[week]) {
        return nutritionChart[week];
    }

    // Find closest available week data based on trimester
    const trimester = week <= 12 ? 1 : week <= 27 ? 2 : 3;
    const weeks = Object.keys(nutritionChart).map(w => parseInt(w)).sort((a, b) => a - b);

    let closest = weeks[0];
    for (const w of weeks) {
        const wTrimester = w <= 12 ? 1 : w <= 27 ? 2 : 3;
        if (wTrimester === trimester) {
            closest = w;
        }
        if (w > week) break;
    }

    return nutritionChart[closest] || nutritionChart[1];
}

